#!/usr/bin/python3

# Original string
word = "Holberton"

# Slice first 3 letters
word_first_3 = word[:3]

# Slice last 2 letters
word_last_2 = word[-2:]

# Slice middle letters
middle_word = word[1:-1]

# Prin result
print("First 3 letters: {}".format(word_first_3))
print("Last 2 letters: {}".format(word_last_2))
print("Middle word: {}".format(middle_word))
